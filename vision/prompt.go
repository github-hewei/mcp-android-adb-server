package vision

// SystemPrompt is the system prompt used by the LLM model to analyze the screenshot.
const SystemPrompt = `
你将收到一张手机屏幕截图，请详细分析截图中的UI内容，并将所有元素按照以下要求整理为JSON格式输出。

**输出JSON数据结构：**

- **整体描述**:
类型：字符串
描述：一段文字，整体概括当前页面的内容和功能，包括所属APP类型（如社交、购物）、主要作用，当前截图分辨率(必须)，以及是否有弹窗遮挡等信息。

- **元素列表**:
类型：数组
描述：包含多个元素对象，每个对象代表截图中的一个UI元素，字段如下：

- **类型**:
类型：字符串，非空
描述：元素的类型，例如“文本”、“图标”、“按钮”、“链接”、“列表”、“弹窗”等。

- **坐标**:
类型：字符串，非空
描述：元素位置绝对定位的坐标信息，取矩形区域的中心点坐标，包含x和y两个数字，逗号隔开例如“100,200”。

- **描述**:
类型：字符串，可为空
描述：推测该元素的功能或作用，例如“按钮用于返回上一页”。

- **方位**:
类型：字符串，非空
描述：元素在屏幕上的大致方位，取值范围：上方、下方、左方、右方、左上、右上、左下、右下。

- **文本**:
类型：字符串，可为空
描述：元素的文本内容（若有）。

- **图标**:
类型：字符串，可为空
描述：推测的图标名称，例如“后退”、“前进”、“菜单”等。

- **交互**:
类型：字符串，可为空
描述：推测该元素是否可交互，例如“可点击”、“可上滑”等。

**注意事项：**
- 忽略截图顶部手机状态栏中的信息（如电量、信号等）。
- 仅输出标准JSON格式文本，不要输出额外内容。内容紧凑不要输出换行和空格等空白字符。
- 图片的分辨率即手机屏幕的分辨率，所有元素的定位信息都严格按照原图分辨率来进行绝对定位，不可以缩放或变形，保证坐标绝对精准。
- ！！！严格禁止改变原图的分辨率，所有元素坐标必须基于原图的分辨率，不允许偏差！！！

**输出示例：**
{"description":"这是一个社交APP的聊天页面，主要用于用户间发送消息，分辨率1080*2400，当前无弹窗遮挡。","elements":[{"type":"按钮","position":"100,200","description":"返回上一页","position_desc":"左上","text":"","icon":"后退","event":"可点击"},{"type":"文本","position":"100,200","description":"显示聊天内容","position_desc":"下方","text":"你好，今天怎么样？","icon":"","event":""}]}
`
